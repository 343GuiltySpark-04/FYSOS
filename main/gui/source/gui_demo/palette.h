#define GUIRGB(r, g, b) ((((bit32u) (r)) << 16) | \
                        (( (bit32u) (g)) <<  8) | \
                        (( (bit32u) (b))))

#define GUITRGB(t, r, g, b) ((((bit32u) (t)) << 24) | \
                             (((bit32u) (r)) << 16) | \
                             (((bit32u) (g)) <<  8) | \
                             (((bit32u) (b))))

#define GUIT(c) ((bit8u) (((c) >> 24) & 0xFF))
